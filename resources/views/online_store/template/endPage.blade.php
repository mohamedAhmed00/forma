<script>
    $(document).ready(function (){
        $('.addToCarts').click(function () {
            $.ajax({
                type: "get",
                url: $("#url").val()+ '/' + $(this).attr("data-product_id"),
                data: {},
                success: function( data ) {
                    $("#cart_con").html(data.res);
                    $.notify("You Add Product To Cart ", "success");
                }
            });
        });
        $('body').on('click','.remove_from_cart_button',function (){
            $.ajax({
                type: "post",
                url: $("#url").val()+ '/' + $(this).attr("data-title"),
                data: {
                    _token:'{{ csrf_token() }}',
                    _method:'DELETE'
                },
                success: function( data ) {
                    $("#cart_con").html(data.res);
                    $.notify("You remove Product From Cart ", "warn");
                }
            });
        });

        $('.addToWishlist').click(function () {
            $.ajax({
                type: "get",
                url: $("#urlwishlist").val()+ '/' + $(this).attr("data-product_id"),
                data: {},
                success: function( data ) {
                    $("#wishlistCount").html(data.count);
                    if(data.action == 'remove') {
                        $.notify("You remove Product From wishlist ", "warn");
                    }
                    else
                    {
                        $.notify("You Add Product To Wishlist ", "success");
                    }
                }
            });
        });
        $('.addTocompare').click(function () {
            $.ajax({
                type: "get",
                url: $("#urlcompare").val()+ '/' + $(this).attr("data-product_id"),
                data: {},
                success: function( data ) {
                    $("#compareCount").html(data.count);
                    if(data.action == 'remove') {
                        $.notify("You remove Product From compare ", "warn");
                    }
                    else
                    {
                        $.notify("You Add Product To compare ", "success");
                    }
                }
            });
        });

    });

</script>
